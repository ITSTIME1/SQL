-- 내림차순이 역순이라는 것.
-- 프로그래밍에서 역순이라고 하면 거꾸로 출력하는 걸 의미하는데
-- 따라서 여기에서도 역순이라는걸 정렬이라고 표현하면 잘못 된 것 같다.
-- 왜냐하면 해당 테이블의 ANIMAL_ID 값은 이미 정렬이 되어 있는 상태기 때문에 오름 차순으로
-- 따라서 이런 경우 거꾸로 출력하고 싶다면 당연히 내림 차순으로 정렬하게 되면 거꾸로 출력하게 되기 떄문에
-- 테이블의 상태가 정렬이 되어 있었기 때문에 가능한 구문이다.
-- 만약 테이블의 상태를 오름차순으로 정렬이 되어 있지 않았다면 역순으로 정렬하는 것은 DESC로 하는게 틀리다.
-- 테이블의 상태가 오름차순으로 정렬이 되어 있지 않다는 것은 무작위인 상태로 정렬이 되어 있을 수 있기 때문에
-- 무작위인 상태를 DESC를 한다고 해서 그게 역순이 되지 않기 때문이다.
-- 역순이라고 한다면 거꾸로 라는 말과 동일한데 처음과 끝을 변경해서 출력하는 걸 뜻한다. 순차적으로
-- 그렇기 때문에 무작위로 변경된 상태에서 끝에서부터 출력해야 역순으로 출력하는 의미를 갖게 되는거지
-- DESC는 말그대로 정렬이라는 의미를 가지고 있기 때문에 문자면 문자 순서대로 정렬이되고
-- 숫자면 숫자의 크기대로 정렬이 된다.
SELECT NAME, DATETIME from ANIMAL_INS order by ANIMAL_ID DESC;
